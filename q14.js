// Função que calcula a soma de dois números
function somarNumeros(num1, num2) {
    const soma = num1 + num2;  // Soma os dois números
    return soma;  // Retorna o resultado da soma
  }
  
  // Definindo dois números
  const numero1 = 10;
  const numero2 = 20;
  
  // Chamando a função e armazenando o valor retornado
  const resultado = somarNumeros(numero1, numero2);
  
  // Exibindo o resultado no console
  console.log("A soma dos números é:", resultado);
  